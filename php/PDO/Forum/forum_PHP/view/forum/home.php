<h2>BIENVENUE</h2>

<p>
    Ce projet a pour but de mettre en application les concepts essentiels de la programmation web : HTML, CSS, JS, POO, SQL et Design Pattern MVC.<br/>
    Ce forum permettra une communication souple et intuitive entre les utilisateurs autorisés à y accéder, c'est-à-dire que leur inscription à la plate-forme est auparavant obligatoire, tout en demeurant simple d'accès.<br/>
    Le public visé est particulièrement hétérogène : les habitués de l'outil informatique comme les novices devront profiter d'une ergonomie et d'un rendu visuel clairs et conformes aux standards utilisés dans la conception d'applications destinées au web.<br/>
</p>